public class Pontos {
    
    float Pi, comprimento, disponível, Pf;

    public float getPi() {
        return Pi;
    }

    public void setPi(float Pi) {
        this.Pi = Pi;
    }

    public float getComprimento() {
        return comprimento;
    }

    public void setComprimento(float comprimento) {
        this.comprimento = comprimento;
    }

    public float getDisponível() {
        return disponível;
    }

    public void setDisponível(float disponível) {
        this.disponível = disponível;
    }

    public float getPf() {
        return Pf;
    }

    public void setPf(float Pf) {
        this.Pf = Pf;
    }
    
    
       
}
